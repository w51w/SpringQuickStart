public class SamsungTV {
	public void powerOn() {
		System.out.println("伙己TV 傈盔 脑");
	}
	public void powerOff() {
		System.out.println("伙己TV 傈盔 阐");
	}
	public void volumeUp() {
		System.out.println("伙己TV 家府 棵覆");
	}
	public void volumeDown() {
		System.out.println("伙己TV 家府 郴覆");
	}
}
