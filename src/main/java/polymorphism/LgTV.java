package polymorphism;

public class LgTV {
	public void turnOn() {
		System.out.println("LgTV 전원 켬");
	}
	public void turnOff() {
		System.out.println("LgTV 전원 끔");
	}
	public void soundUp() {
		System.out.println("LgTV 소리 올림");
	}
	public void soundDown() {
		System.out.println("LgTV 소리 내림");
	}
}
